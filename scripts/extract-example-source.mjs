#!/usr/bin/env node
// Extract the JSX children of every <Example>...</Example> in each
// src/components/examples/*.tsx file. For each input file, write a sibling
// generated module at src/generated/example-source/<name>.ts. Used by
// Example.tsx at runtime to display (and copy) the live source for each
// demo, with state values substituted in.

import {
  readdirSync,
  readFileSync,
  writeFileSync,
  mkdirSync,
  rmSync,
  existsSync,
} from 'node:fs';
import { basename, dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const examplesDir = resolve(projectRoot, 'src/components/examples');
const outDir = resolve(projectRoot, 'src/generated/example-source');

// Wipe the output dir so deleted/renamed example files don't leave stale
// generated modules behind.
if (existsSync(outDir)) rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

const files = readdirSync(examplesDir).filter(
  (f) => f.endsWith('.tsx') && !f.startsWith('__'),
);

let totalExamples = 0;
for (const file of files) {
  const raw = readFileSync(join(examplesDir, file), 'utf8');
  const examples = extractExamples(raw);
  const name = basename(file, '.tsx');
  const outFile = join(outDir, `${name}.ts`);
  writeFileSync(
    outFile,
    '// Generated by scripts/extract-example-source.mjs — do not edit\n\n' +
      'export const EXAMPLE_SOURCE: Record<string, string> = {\n' +
      Object.entries(examples)
        .map(([n, code]) => `  ${n}: ${JSON.stringify(code)},`)
        .join('\n') +
      '\n};\n',
  );
  totalExamples += Object.keys(examples).length;
}

console.log(
  `extract-example-source: wrote ${totalExamples} examples across ${files.length} files to ${outDir.replace(projectRoot + '/', '')}/`,
);

// ---------------------------------------------------------------------------

function extractExamples(source) {
  const result = {};
  const fnRegex = /export function (\w+Example)\s*\(/g;
  let match;
  while ((match = fnRegex.exec(source))) {
    const fnName = match[1];
    const tagOpen = source.indexOf('<Example', match.index);
    if (tagOpen === -1) continue;

    const openEnd = findOpenTagEnd(source, tagOpen + '<Example'.length);
    if (openEnd === -1) continue;
    if (source[openEnd - 1] === '/') continue; // self-closing — no children

    const closeStart = findMatchingClose(source, openEnd + 1, 'Example');
    if (closeStart === -1) continue;

    const children = dedent(source.slice(openEnd + 1, closeStart)).trim();
    result[fnName] = children;
  }
  return result;
}

// Walk forward from inside an opening tag, accounting for string literals and
// JSX expression braces, to find the `>` that closes it.
function findOpenTagEnd(source, start) {
  let i = start;
  let braceDepth = 0;
  let str = null;
  while (i < source.length) {
    const ch = source[i];
    if (str) {
      if (ch === '\\') {
        i += 2;
        continue;
      }
      if (ch === str) str = null;
    } else if (ch === '"' || ch === "'") {
      str = ch;
    } else if (ch === '{') {
      braceDepth++;
    } else if (ch === '}') {
      braceDepth--;
    } else if (braceDepth === 0 && ch === '>') {
      return i;
    }
    i++;
  }
  return -1;
}

// Find the index of the matching `</tagName>`, accounting for nested tags of
// the same name and for JSX expression braces.
function findMatchingClose(source, start, tagName) {
  const open = '<' + tagName;
  const close = '</' + tagName + '>';
  let depth = 1;
  let i = start;
  let braceDepth = 0;
  let str = null;
  while (i < source.length) {
    const ch = source[i];
    if (str) {
      if (ch === '\\') {
        i += 2;
        continue;
      }
      if (ch === str) str = null;
      i++;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === '`') {
      str = ch;
      i++;
      continue;
    }
    if (ch === '{') {
      braceDepth++;
      i++;
      continue;
    }
    if (ch === '}') {
      braceDepth--;
      i++;
      continue;
    }
    if (braceDepth > 0) {
      i++;
      continue;
    }
    if (source.startsWith(close, i)) {
      depth--;
      if (depth === 0) return i;
      i += close.length;
      continue;
    }
    if (source.startsWith(open, i)) {
      const next = source[i + open.length];
      if (next === ' ' || next === '\n' || next === '\t' || next === '>') {
        depth++;
        i += open.length;
        continue;
      }
    }
    i++;
  }
  return -1;
}

function dedent(text) {
  const lines = text.split('\n');
  const indents = lines
    .filter((l) => l.trim().length > 0)
    .map((l) => l.match(/^[ \t]*/)[0].length);
  if (indents.length === 0) return text;
  const min = Math.min(...indents);
  return lines.map((l) => l.slice(min)).join('\n');
}
