#!/usr/bin/env node
// Emit src/generated/mcp-catalog.ts — the flat directory the MCP server
// returns from list_components / search. Each entry pairs a doc page's
// slug with its DocsLayout `title` and `description`, taken straight from
// the .mdx source so the catalog stays in sync as docs are added or
// reworded. Bucketed by kind so the MCP can route get_component vs.
// get_foundation vs. get_hook correctly.

import { readFileSync, readdirSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const pagesRoot = resolve(projectRoot, 'src/pages/react');
const outFile = resolve(projectRoot, 'src/generated/mcp-catalog.ts');

const SECTIONS = [
  { dir: 'components', kind: 'component' },
  { dir: 'foundations', kind: 'foundation' },
  { dir: 'hooks', kind: 'hook' },
];

const entries = [];
for (const { dir, kind } of SECTIONS) {
  const fullDir = join(pagesRoot, dir);
  for (const file of readdirSync(fullDir).sort()) {
    if (!file.endsWith('.mdx') || file === 'index.mdx') continue;
    const slug = file.replace(/\.mdx$/, '');
    const raw = readFileSync(join(fullDir, file), 'utf8');
    const layoutMatch = raw.match(/<DocsLayout([\s\S]*?)>/);
    if (!layoutMatch) continue;
    const attrs = layoutMatch[1];
    const title = pickAttr(attrs, 'title')?.replace(
      /\s+[—-]\s+Cladd$/,
      '',
    );
    const description = pickAttr(attrs, 'description');
    if (!title || !description) continue;
    entries.push({ kind, slug, name: title, description });
  }
}

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, render(entries));

console.log(
  `[generate-mcp-catalog] wrote ${entries.length} catalog entries → ${
    outFile.replace(projectRoot + '/', '')
  }`,
);

function pickAttr(attrs, name) {
  const m = attrs.match(
    new RegExp(`\\b${name}\\s*=\\s*"((?:[^"\\\\]|\\\\.)*)"`),
  );
  return m ? m[1] : null;
}

function render(rows) {
  const body = rows
    .map(
      (r) =>
        `  { kind: ${JSON.stringify(r.kind)}, slug: ${JSON.stringify(
          r.slug,
        )}, name: ${JSON.stringify(r.name)}, description: ${JSON.stringify(
          r.description,
        )} },`,
    )
    .join('\n');
  return [
    '// Auto-generated by scripts/generate-mcp-catalog.mjs.',
    '// Do not edit by hand — re-run `npm run codegen` instead.',
    '',
    "export type CatalogKind = 'component' | 'foundation' | 'hook';",
    '',
    'export interface CatalogEntry {',
    '  kind: CatalogKind;',
    '  slug: string;',
    '  name: string;',
    '  description: string;',
    '}',
    '',
    'export const MCP_CATALOG: readonly CatalogEntry[] = [',
    body,
    '];',
    '',
  ].join('\n');
}
