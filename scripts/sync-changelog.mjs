#!/usr/bin/env node
// Mirror ../cladd/CHANGELOG.md into src/pages/react/changelog.mdx so the
// release notes page stays in sync with whatever the package itself ships.
// Runs as part of `codegen`, so dev/build always reflect the current
// CHANGELOG without a manual copy step.
//
// The source file is plain Markdown but lives inside an MDX page, so a few
// characters need escaping (notably bare `<…>` tag-like strings — the
// CHANGELOG mentions `<kbd>` in a release note, which MDX would otherwise
// try to parse as JSX).

import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = resolve(projectRoot, '../cladd/CHANGELOG.md');
const outPath = resolve(projectRoot, 'src/pages/changelog.mdx');

if (!existsSync(sourcePath)) {
  console.warn(
    `[sync-changelog] ${relative(projectRoot, sourcePath)} not found — skipping`,
  );
  process.exit(0);
}

const raw = readFileSync(sourcePath, 'utf8');

// Drop the source's `# Changelog` heading — we render our own below so the
// page has a single top-level title.
const body = raw.replace(/^#\s+Changelog\s*\n+/, '').trimEnd();

// Escape `<` only when it's followed by an alphabetic character — those are
// the cases MDX would mistake for a JSX tag. Plain comparisons (`a < b`),
// arrow notation (`->`), and other uses are left alone. The CHANGELOG has no
// fenced code blocks today, so blanket-escaping outside fences is fine; if
// that ever changes, this needs a fence-aware pass.
const escaped = body.replace(/<(?=[A-Za-z/])/g, '&lt;');

const mdx = `import { PageLayout } from '@/components/PageLayout';

export default ({ children }) => (
  <PageLayout
    title="Changelog — Cladd"
    description="Release notes for @cladd-ui/react."
  >
    {children}
  </PageLayout>
);

{/* This page is generated by scripts/sync-changelog.mjs from ../cladd/CHANGELOG.md. Do not edit by hand. */}

# Changelog

${escaped}
`;

writeFileSync(outPath, mdx);

console.log(
  `[sync-changelog] wrote ${relative(projectRoot, outPath)} from ${relative(projectRoot, sourcePath)}`,
);
